package day27_nested_loops;

public class NestedLoopWithBreakAndContinue {
    public static void main(String[] args) {

        for(int i= 1; i <= 3; i++){

            System.out.println("first");


            for (int j = 1; j <= 2; j++){

                System.out.println("second");

            }


        }


    }
}
